package mt.mentalistFrontend.Util;

/**
 * Utilidades reutilizables para el frontend (Java Swing).
 *
 * Clases disponibles:
 * - AlertaUtils.java           → Muestra mensajes centralizados con JOptionPane (éxito, error, advertencia).
 * - ComboUtils.java            → Métodos para llenar y leer datos de JComboBox (ej. enums o listas).
 * - FechaUtils.java            → Conversión entre objetos Date/String y formatos legibles.
 * - JsonUtils.java             → Serialización y deserialización de objetos usando Gson (JSON ↔ Objeto).
 * - PathUtils.java             → (Opcional) Manejo de rutas locales para guardar archivos PDF o JSON.
 * - PlaceholderSupport.java    → Aplicar textos sugeridos (placeholder) a campos JTextField.
 * - SesionUsuario.java         → Almacena el token JWT, el nombre y rol del usuario autenticado.
 * - TablaUtils.java            → (Opcional) Métodos para limpiar, obtener filas o trabajar con JTable.
 * - ValidadorCampos.java       → Validaciones comunes de formularios: campos vacíos, correo, número, etc.
 */