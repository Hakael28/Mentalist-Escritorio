package mt.mentalistFrontend.Modelo;

/**
 * DTOs utilizados por el frontend para enviar o recibir datos del backend.
 *
 * Clases disponibles:
 * - UsuarioDTO.java                → Información del usuario autenticado o administrado.
 * - PacienteDTO.java               → Datos personales del paciente (documento, nombres, edad, etc.).
 * - ReporteDTO.java                → Reportes realizados por usuarios con fecha, tipo y comentarios.
 * - CasoDTO.java                   → Información de casos de salud mental (tipo, gravedad, fecha).
 * - HistoriaClinicaDTO.java        → Datos clínicos relacionados con el paciente.
 * - DiagnosticoEspecificoDTO.java  → Diagnósticos detallados aplicados al paciente.
 * - RutaAtencionDTO.java           → Servicios y entidades que atendieron al paciente.
 * - CursoVidaDTO.java              → Información del curso de vida (Edad del apciente).
 * - AreaOcurrenciaDTO.java         → Lugar donde ocurrió el caso (hogar, calle, institución).
 * - EapbDTO.java                   → Datos de la EPS o entidad de aseguramiento.
 *
 * Subpaquete enums:
 * - Rol.java                       → Roles del sistema (ADMIN, MEDICO).
 * - TipoReporte.java               → Tipos de reporte disponibles.
 * - TipoDocumento.java             → Tipos de documento válidos (CC, TI, etc.).
 * - Genero.java                    → Géneros disponibles (MASCULINO, FEMENINO, OTRO).
 * - Etapa.java                     → Etapas del ciclo vital (INFANCIA, ADOLESCENCIA, ADULTEZ).
 */
